public class FreeTicket_INOI1402 {

	public static void main(String[] args) {
		int m=5;
		int n=5;
		int[][]a=new int[m][n];
		for(int i=0;i<m;i++){
			for(int j=0;j<m;j++){
				System.out.print(a[i][j]+" ");
			}
			System.out.println();
		}
		
	}

}
